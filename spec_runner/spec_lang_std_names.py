def namespace_index() -> dict[str, set[str]]:
    out: dict[str, set[str]] = {}
    for fq in STD_TO_FLAT:
        parts = fq.split(".")
        ns = ".".join(parts[:-1])
        out.setdefault(ns, set()).add(parts[-1])
    return out
